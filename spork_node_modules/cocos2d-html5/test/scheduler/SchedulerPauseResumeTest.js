var SchedulerPauseResumeTest = cc.Layer.extend({
    ctor : function(){
        this._super();

        this.schedule(this.onTick1, 0.5);
        this.schedule(this.onTick2, 0.5);
        this.schedule(this.onPause, 3);
    },
    onTick1:function (dt) {
        logTest("tick1");
    },
    onTick2:function (dt) {
        logTest("tick2");
    },
    onPause:function (dt) {
        cc.Director.getInstance().getScheduler().pauseTarget(this);
    }
});

SchedulerPauseResumeTest.create = function(args){
    var layer = new SchedulerPauseResumeTest();
    return layer.init() ? layer : null;
};
