var BMFontTest = cc.Layer.extend({
    labelLeft:null,
    labelCenter:null,
    labelRight:null,

    ctor:function () {
        this._super();

        var LongSentencesExample = "Lorem ipsum dolor sit amet, consectetur adipisicing elit, sed do eiusmod tempor incididunt ut labore et dolore magna aliqua.";
        var LineBreaksExample = "Lorem ipsum dolor\nsit amet\nconsectetur adipisicing elit\nOHLALA\nOHLALA";
        var MixedExample = "ABC\nLorem ipsum dolor sit amet, consectetur adipisicing elit, sed do eiusmod tempor incididunt\nDEF";

        // ask director the the window size
        var size = cc.Director.getInstance().getWinSize();

        this.labelCenter = cc.LabelBMFont.create(LongSentencesExample, res.markerFelt_fnt, size.width / 3, cc.TEXT_ALIGNMENT_CENTER, cc.p(0, 0));
        // position the label on the center of the screen
        this.labelCenter.setPosition(size.width / 2, size.height / 2);

        this.labelLeft = cc.LabelBMFont.create(LineBreaksExample, res.markerFelt_fnt, size.width / 3 - 20, cc.TEXT_ALIGNMENT_LEFT, cc.p(0, 0));
        // position the label on the top left of the screen
        this.labelLeft.setPosition(10, size.height - 10);
        this.labelLeft.setAnchorPoint(0, 1);

        this.labelRight = cc.LabelBMFont.create(MixedExample, res.markerFelt_fnt, size.width / 3 - 20, cc.TEXT_ALIGNMENT_RIGHT, cc.p(0, 0));
        // position the label on the bottom right of the screen
        this.labelRight.setPosition(size.width - 10, 10);
        this.labelRight.setAnchorPoint(1, 0);

        this.addChild(this.labelLeft);
        this.addChild(this.labelCenter);
        this.addChild(this.labelRight);

    }
});

BMFontTest.create = function(args){
    var layer = new BMFontTest();
    return layer;
}