var LabelTTFChinese = cc.Layer.extend({
    ctor : function(){
        this._super();
        var winSize = cc.Director.getInstance().getWinSize();

        var label = cc.LabelTTF.create("中国", "Microsoft Yahei", 30);
        label.setPosition(winSize.width / 2, winSize.height / 3 * 2);
        this.addChild(label);

        // Test UTF8 string from native to jsval.
        var label2 = cc.LabelTTF.create("string from native:" + label.getString(), "Microsoft Yahei", 30);
        label2.setPosition(winSize.width / 2, winSize.height / 3);
        this.addChild(label2);
    }
});

LabelTTFChinese.create = function(args){
    var layer = new LabelTTFChinese();
    return layer.init() ? layer : null;
};
