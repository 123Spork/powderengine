var TMXOrthoTest3 = cc.Layer.extend({
    init : function(){
        this._super();

        var map = cc.TMXTiledMap.create(res.orthogonal_test3_tmx);
        this.addChild(map, 0, 1);

        map.setScale(0.2);
        map.setAnchorPoint(0.5, 0.5);

        return true;
    }
});

TMXOrthoTest3.create = function(args){
    var layer = new TMXOrthoTest3();
    return layer.init() ? layer : null;
};