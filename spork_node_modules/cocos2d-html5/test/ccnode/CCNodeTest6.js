var CCNodeTest6 = cc.Layer.extend({
    ctor : function(){
        this._super();

        var winSize = cc.Director.getInstance().getWinSize();
        var sp1 = cc.Sprite.create(res.grossinis_sister1_png);
        var sp11 = cc.Sprite.create(res.grossinis_sister1_png);

        var sp2 = cc.Sprite.create(res.grossinis_sister2_png);
        var sp21 = cc.Sprite.create(res.grossinis_sister2_png);

        sp1.setPosition(150, winSize.height / 2);
        sp2.setPosition(winSize.width - 150, winSize.height / 2);

        this.addChild(sp1, 0, 1);
        sp1.addChild(sp11, 11);
        this.addChild(sp2, 0, 2);
        sp2.addChild(sp21, 21);

        this.schedule(this.onAddAndRemove, 2.0);
    },

    onAddAndRemove:function (dt) {
        var sp1 = this.getChildByTag(1);
        var sp2 = this.getChildByTag(2);

        this.removeChild(sp1, false);
        this.removeChild(sp2, true);

        this.addChild(sp1, 0, 1);
        this.addChild(sp2, 0, 2);
    }
});

CCNodeTest6.create = function(args){
    var layer = new CCNodeTest6();
    return layer.init() ? layer : null;
};
