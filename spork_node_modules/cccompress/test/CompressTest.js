var CompressTest = cc.Layer.extend({
    init : function(){
        this._super();
        var label = cc.LabelTTF.create("No test case to display!")
        var winSize = cc.Director.getInstance().getWinSize();
        this.addChild(label);
        label.setPosition(winSize.width/2, winSize.height/2);
        return true;
    }
});
CompressTest.create = function(args){
    var layer = new CompressTest();
    layer.init();
    return layer;
};