var MusicTest = cc.Layer.extend({
    init : function(){
        this._super();
        custDiv.innerHTML = "";
        var self = this;
        audioEngine = cc.AudioEngine.getInstance();

        var btnContainer1 = createBtnContainer(custDiv);
        var btnContainer2 = createBtnContainer(custDiv);

        createBtn(btnContainer1, "Play", function(){
            audioEngine.playMusic(res.background_mp3, false);
        });
        createBtn(btnContainer1, "Pause", function(){
            audioEngine.pauseMusic();
        });
        createBtn(btnContainer1, "Stop", function(){
            audioEngine.stopMusic();
        });
        createBtn(btnContainer1, "Rewind", function(){
            audioEngine.rewindMusic();
        });
        createBtn(btnContainer1, "Resume", function(){
            audioEngine.resumeMusic();
        });
        createBtn(btnContainer2, "Volume +", function(){
            audioEngine.setMusicVolume(audioEngine.getMusicVolume() + 0.1);
            logTest("Current volume is : " + audioEngine.getMusicVolume());
        });
        createBtn(btnContainer2, "Volume -", function(){
            audioEngine.setMusicVolume(audioEngine.getMusicVolume() - 0.1);
            logTest("Current volume is : " + audioEngine.getMusicVolume());
        });

        return true;
    },

    onExit : function(){
        audioEngine.stopMusic();
    }
});
MusicTest.create = function(args){
    var layer = new MusicTest();
    layer.init();
    return layer;
} 