var TMXIsoTest = cc.Layer.extend({
    init : function(){
        this._super();

        var color = cc.LayerColor.create(cc.c4b(64, 64, 64, 255));
        this.addChild(color, -1);

        var map = cc.TMXTiledMap.create(res.iso_test_tmx);
        this.addChild(map, 0, 1);

        // move map to the center of the screen
        var ms = map.getMapSize();
        var ts = map.getTileSize();
        map.setPosition(-ms.width * ts.width / 2, -ms.height * ts.height / 2);

        return true;
    }
});

TMXIsoTest.create = function(args){
    var layer = new TMXIsoTest();
    return layer.init() ? layer : null;
};
