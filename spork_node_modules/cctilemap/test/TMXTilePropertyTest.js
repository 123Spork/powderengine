var TMXTilePropertyTest = cc.Layer.extend({
    propertiesList:[],
    init : function(){
        this._super();

        var map = cc.TMXTiledMap.create(res.ortho_tile_property_tmx);
        this.addChild(map, 0, 1);

        for (var i = 1; i <= 6; i++) {
            var properties = map.propertiesForGID(i);
            logTest("GID:" + i + ", Properties:" + JSON.stringify(properties));
            this.propertiesList.push(properties)
        }
        return true;
    }
});

TMXTilePropertyTest.create = function(args){
    var layer = new TMXTilePropertyTest();
    return layer.init() ? layer : null;
};
