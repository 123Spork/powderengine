
var RenderTextureTest = cc.Layer.extend({
    init: function () {
        var winSize = cc.Director.getInstance().getWinSize();
        this._super();
        var texture1 = cc.RenderTexture.create(winSize.width, winSize.height);
        texture1.setPosition(winSize.width / 2, winSize.height / 2);
        var sprite = cc.Sprite.create(res.fire_png);
        sprite.retain();//only use for jsb

        this.addChild(texture1, 1);
        texture1.begin();
        for(var i = 0;i<20;i++){
            sprite.setPosition(cc.p(i*30,winSize.height/2));
            sprite.setRotation(Math.random() * 360);
            sprite.setScale(Math.random() * 2);
            sprite.setColor(cc.c3b(Math.random() * 255, 255, 255));
            sprite.visit();
        }
        texture1.end();
        return true;
    }
});
RenderTextureTest.create = function(args){
    var layer = new RenderTextureTest();
    return layer.init() ? layer : null;
}