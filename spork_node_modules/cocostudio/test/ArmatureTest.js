
var ArmatureTest = cc.LayerGradient.extend({
    init : function(){
        this._super(cc.c4b(98,99,117,255), cc.c4b(0,0,0,255));

        var winSize = cc.Director.getInstance().getWinSize();

        ccs.ArmatureDataManager.getInstance().addArmatureFileInfo(res.Cowboy_ExportJson);

        var armature = ccs.Armature.create("Cowboy");
        armature.getAnimation().playWithIndex(0);
        armature.setScale(0.2);
        armature.setAnchorPoint(0.5, 0.5);
        armature.setPosition(winSize.width / 2, winSize.height / 2);
        this.addChild(armature);
        return true;
    }
});

ArmatureTest.create = function(args){
    var layer = new ArmatureTest();
    layer.init();
    return layer;
};